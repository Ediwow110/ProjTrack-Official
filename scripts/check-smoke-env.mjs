#!/usr/bin/env node
// Pre-flight guard for npm run e2e:smoke.
// Prevents skip-green smoke runs by failing loudly when required admin smoke
// env vars are missing. Teacher/student credentials are generated during
// seed:smoke unless optional local overrides are supplied.
import process from "node:process";

const REQUIRED = [
  "SMOKE_ADMIN_IDENTIFIER",
  "SMOKE_ADMIN_PASSWORD",
];

const missing = REQUIRED.filter((name) => {
  const value = process.env[name];
  return value === undefined || String(value).trim() === "";
});

if (missing.length === 0) {
  console.log("[check:smoke-env] Admin smoke credentials present.");
  console.log(
    "[check:smoke-env] Teacher/student smoke credentials will be generated by seed:smoke.",
  );
  process.exit(0);
}

console.error("[check:smoke-env] Required admin smoke env vars are missing:");
for (const name of missing) console.error(`  - ${name}`);
console.error("");
console.error(
  "Real smoke requires admin credentials up front. Teacher/student smoke credentials are generated during seed:smoke.",
);
console.error("Values are never printed here, only missing variable names.");
console.error("Use test-only or staging-only credentials. Never use production credentials.");
console.error("");
console.error("GitHub Actions:");
console.error("  GitHub repo -> Settings -> Secrets and variables -> Actions -> New repository secret");
console.error("  Add the missing admin smoke secrets, then rerun CI and Production Checks.");
console.error("");
console.error("PowerShell:");
console.error('  $env:SMOKE_ADMIN_IDENTIFIER   = "admin.smoke@example.test"');
console.error('  $env:SMOKE_ADMIN_PASSWORD     = "<strong test-only password>"');
console.error("");
console.error("Bash:");
console.error('  export SMOKE_ADMIN_IDENTIFIER=admin.smoke@example.test');
console.error("  export SMOKE_ADMIN_PASSWORD='<strong test-only password>'");
console.error("");
console.error("Then provision fixtures and run smoke:");
console.error("  npm run seed:smoke");
console.error("  npm run e2e:smoke");
console.error("");
console.error("Notes:");
console.error("  * These vars are for local or staging only. Never reuse production credentials.");
console.error("  * In CI, set them as GitHub Actions secrets, never inline in YAML.");
console.error(
  "  * seed:smoke generates teacher/student credentials and writes them to .tmp/smoke-credentials.json.",
);
console.error(
  "  * Optional local overrides still exist for SMOKE_TEACHER_* and SMOKE_STUDENT_*, but they are not required.",
);
console.error("  * npm run e2e:responsive does NOT require these vars; it tests auth pages only.");
process.exit(1);
